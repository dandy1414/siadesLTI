<li>Keluarga tidak mampu menyekolahkan anak yang berumur 7 tahun s/d 15 tahun.</li>
<li>Jumlah kekayaan milik keluarga (diluar tanah dan bangunan) Rp 2.500.000,00.</li>
<li>Tanah dan bagunan yang ditempati bukan milik sendiri.</li>
<li>Tidak menggunakan air bersih untuk keperluan makan minum dan MCK.</li>
<li>Tidak menggunakan listrik untuk kebutuhan rumah tangga.</li>
<li>Jumlah anggota atau jiwa dalam KK (termasuk kepala keluarga) lima jiwa atau lebih.</li>
</ol>
<p><strong>8&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong><strong>Ketenagakerjaan</strong></p>
<p>Jumlah penduduk usia kerja pada tahun 2013 di Pendowoharjo mencapai 14.694 orang, yang sudah terserap dalam lapangan kerja 10.654 meliputi : PNS/TNI/POLRI, Pertanian, Pertukangan, Buruh, Karyawan Swasta, Pengrajin, Peternakan dan pekerjaan lainnya. Sedangkan yang belum memiliki pekerjaan berjumlah 4.040 orang.</p>
