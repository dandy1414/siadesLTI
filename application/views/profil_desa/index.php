<p><strong>1.</strong><strong>&nbsp; Sejarah Desa Pendowoharjo</strong></p>
<p>Desa Pendowoharjo lahir tepat pada tanggal 26 Desember 1946 (setelah digabung) yang asal mulanya berasal dari 5(lima) kalurahan lama antara lain:</p>
<p>1)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Karanggede (dengan sebutan Kring) terdiri dari Pedukuhan&nbsp; Dagen dan Pedukuhan&nbsp; Cepit.</p>
<p>2)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ngrukem (kring Ngrukem) terdiri dari Pedukuhan Sawahan, Pedukuhan Krandohan, Pedukuhan Ngimbang&nbsp; dan Pedukuhan Miri.</p>
<p>3)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Bandung(Kring Bandung) terdiri dari Pedukuhan Bandung, Pedukuhan Ngaglik, Pedukuhan Monggang dan Pedukuhan Kaliputih.</p>
<p>4)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Krantil (Kring Krantil) terdiri dari Pedukuhan Blunyahan , Pedukuhan Pucung dan Pedukuhan Diro.</p>
<p>5)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Pendowo (Kring Pendowo) terdiri dari Pedukuhan Rogoitan, Pedukuhan Banyon dan Pedukuhan Pendowo.</p>
