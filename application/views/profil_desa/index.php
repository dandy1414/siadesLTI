<p>Jika ditinjau dari aspek pertanian, meskipun terjadi perubahan penggunaan lahan sawah namun luas lahan pertanian yang ada masih mampu untuk mencukupi kebutuhan dan ketersediaan pangan bagi masyarakat. Namun demikian alih fungsi lahan tersebut harus dikendalikan secara ketat agar tidak mengancam potensi pertanian dan ketersediaan bahan pangan. Proporsi penggunaan lahan pada tahun 2013</p>
