<p><strong>6&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong><strong>Perekonomian Desa</strong></p>
<p><strong>&nbsp;</strong></p>
<p class="xl36">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 6.1 &nbsp;&nbsp;&nbsp; Kondisi Ekonomi:</p>
<ol>
<li>Potensi Unggulan Desa</li>
</ol>
<p class="Style1">1)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Potensi Pertanian</p>
<p class="Style1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Desa Pendowoharjo memiliki Sawah 333 Ha dengan 16 Kelompok Tani , 7 kelompok P3A dan 1 Kelompok GP3A, 1 kelompok Wanita Tani , 1 kelompok Lembaga Distribusi pangan Masyarakat, 4 kelompok ternak dan semua tergabung dalam kelompok tani tingkat Desa yang disebut Gapoktan.</p>
<p class="Style1">2)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Potensi Industri</p>
<p class="Style1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Desa Pendowoharjo mempunyai produk unggulan yaitu Kipas bambu yang terpusat di Pedukuhan Banyon, besek di Pedukuhan Kaliputih&nbsp; dan kreneng di Pedukuhan Monggang.Tersedianya bahan baku dan tenaga kerja setempat yang cukup selain kipas yang ada batik dan mebelair.Penyembelihan ( Jagal ) di Pedukuhan Krandohan.Pengrajin patung primitiv di Pedukuhan Pucung.</p>
<p class="Style1">3)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Potensi Wisata</p>
<p class="Style1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Potensi Wisata yang bisa dikembangkan di Desa Pendowoharjo : Wana Desa yang terletak di Dusun Cepit dan Arung jeram yang berada di Sungai Bedog yang arusnya sepanjang musim terjaga.</p>
<p class="Style1">4)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Potensi Investasi</p>
<ul>
<li>Wanadesa yang berada di Pedukuhan Cepit.</li>
<li>Telaga Desa yang berada di Pedukuhan Banyon.</li>
<li>Wisata Air / Arungjeram yang berada di Sungai Bedog \sebagai tujuan Wisata</li>
<li>Industri Kipas Bambu Di Pedukuhan Banyon.</li>
</ul>
<ol>
<li>Struktur Perekonomian Desa</li>
</ol>
<p class="Style1">Struktur perekonomian yang berada di Desa Pendowoharjo termasuk sektor yang paling utama mendukung perekonomian Desa antara lain : sektor pertanian masih mempunyai andil terbesar baru disusul sektor perdagangan dan jasa.</p>
<ol>
<li>Lembaga-lembaga Perekonomian Desa</li>
</ol>
<p class="Style1">Lembaga-lembaga Perekonomian yang berada di Desa pendowoharjo diantaranya:</p>
<p class="Style1">1)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; LDPM ( Lembaga Distribusi Pangan Masyarakat )</p>
<p class="Style1">Sebuah lembaga yang berada di Desa dikelola&nbsp; oleh Gapoktan yang bertujuan untuk mengendalikan distribusi pangan masyarakat dan memberikan nilai tambah bagi petani ( dengan operasi tanda jual) yang dikoordinir oleh Gapoktan melalui LDPM</p>
<p class="Style1">2)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; BKM/ KSP</p>
<p class="Style1">Dengan beroperasinya koperasi simpan pinjam di masyarakat dan yang dikelola oleh BKM, maupun oleh kelompok simpan pinjam perempuan diharapkan masyarakat mudah didalam mengakses permodalan didalam mengembangkan usahanya.</p>
<p class="Style1">3)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Bank</p>
<p class="Style1">Dengan beroperasinya bank yang berada di wilayah Desa Pendowoharjo diharapkan masyarakat, lembaga/ kelompok bila mau mengakses permodalan yang lebih besar tidak perlu jauh-jauh ke kabupaten atau bank yang lain.</p>
<p class="Style1">4)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; LKM –PUAP</p>
<p class="Style1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Pengembangan Usaha Ekonomi Pedesaan (PUAP) merupakan salah satu kegiatan yang dikelola oleh Gabungan Kelompok Tani (Gapoktan) Desa pendowoharjo untuk membantu memberdayakan petani kurang mampu agar dapat mengembangkan usahanya (Agribisnis Pertanian)melalui pinjaman modal kelompok pelayanan dilaksanakan 2 (dua)kali dalam sebulan tiap tanggal 15 dan 30.</p>
<p><strong>6.2</strong><strong>&nbsp;&nbsp;&nbsp;&nbsp; Investasi&nbsp; Masyarakat</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Keikutsertaan masyarakat dalam membangun desa baik secara langsung maupun tidak langsung yang tertuang dalam swadaya masyarakat berpengaruh terhadap perkembangan perekonomian desa. Secara garis besar investasi masyarakat ditanamkan dalam kegiatan pembangunan infrastruktur antara lain : pengerasan jalan, perbaikan saluran irigasi, tempat peribadatan, dan lain-lain.</p>
<p><strong>7&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong><strong>. </strong><strong>Keluarga Miskin</strong></p>
<p>Pemerintah Daerah Kabupaten Bantul melakukan kategorisasi keluarga miskin didasarkan atas kriteria yang meliputi beberapa aspek yaitu</p>
<p>7.1&nbsp;&nbsp;&nbsp;&nbsp; Aspek Penentu</p>
<ol>
<li>Seluruh anggota keluarga tidak mampu makan minimal dua kali dalam sehari<ol>
<li>Sebagian besar dari anggota keluarga tidak memiliki pakaian pantas pakai minimal 6 stel.</li>
<li>Tempat tinggal atau rumah berlantai tanah atau berdinding bambu/beratap rumbia.</li>
</ol></li>
</ol>
<p>7.2&nbsp;&nbsp;&nbsp;&nbsp; Aspek Penyebab</p>
<p>Jumlah penghasilan yang diterima seluruh anggota keluarga yang berusia 16&nbsp; tahun keatas (termasuk kepala keluarga) rata-rata per bulan Rp 800.000,-.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 7.3&nbsp;&nbsp;&nbsp;&nbsp; Aspek Pendukung</p>
<ol>
